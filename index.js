function add(a, b) {
  return a + b;
}

function subtract(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function inc(n) {
  return n+1;
}

function dec(n) {
  return n-1;
}

function makeInt(i) {
  x = parseInt(i, 10)
  return x;
}

function preserveDecimal(f) {
  y = parseFloat(f, 10)
  return y;
}
